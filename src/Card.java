
public class Card {
   Actualcard a;
   Rankvalue r;
   Suits s;
   
   
public Card(Actualcard a, Rankvalue r, Suits s) {
	this.a = a;
	this.r = r;
	this.s = s;
 
}




public Actualcard getA() {
	return a;
}




public void setA(Actualcard a) {
	this.a = a;
}




public Rankvalue getR() {
	return r;
}


public void setR(Rankvalue r) {
	this.r = r;
}


public Suits getS() {
	return s;
}


public void setS(Suits s) {
	this.s = s;
}

 
}
